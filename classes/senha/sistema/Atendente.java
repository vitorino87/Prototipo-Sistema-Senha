package senha.sistema;

public class Atendente {
	private String nome;
	private String guiche;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getGuiche() {
		return guiche;
	}
	public void setGuiche(String guiche) {
		this.guiche = guiche;
	}
	
}
